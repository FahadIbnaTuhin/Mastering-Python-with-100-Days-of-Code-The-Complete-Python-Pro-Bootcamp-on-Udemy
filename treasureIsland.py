leftOrRight = input("You're at a cross road. Where do you want to go? Type \"left\" or \"right\"\n").lower()
if leftOrRight == "right":
    print("Fall into a hole.\nGame Over.")
else:
    swimOrWait = input("Swim or Wait? ").lower()
    if swimOrWait == "swim":
        print("Attacked by trout.\nGame Over.")
    else:
        door = input("Which door (Red, Blue, Yellow)? ").lower()
        if door == "red":
            print("Burned by fire.\nGame Over.")
        elif door == "blue":
            print("Eaten by beasts.\nGame Over.")
        elif door == "yellow":
            print("You Win!")
        else:
            print("Game Over.")